function add(number1, number2) {
  return number1 + number2;
}

const sum = add(5, 10);
console.log(sum);

function subtract(number1, number2) {
  return number1 - number2;
}
const difference = subtract(10, 5);
console.log(difference);

function multiply(number1, number2) {
  return number1 * number2;
}

const product = multiply(5, 10);
console.log(product);
